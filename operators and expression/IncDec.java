public class IncDec {
    
    public static void main(String[] args) {
        int x = 5;
        System.out.println("x++ = " + (x++)); // Post increment
        System.out.println("After x++: " + x);
        System.out.println("++x = " + (++x)); // Pre increment
        System.out.println("x-- = " + (x--));
        System.out.println("After x--: " + x);
    }
}

    

